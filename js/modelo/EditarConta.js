class EditarConta {
    getNome() {
        return this.nome;
    };
    setNome(nom) {
        this.nome = nom;
    };
    getEmail() {
        return this.email;
    };
    setEmail(ema) {
        this.email = ema;
    };
    getSenha() {
        return this.senha;
    };
    setSenha(sen) {
        this.senha = sen;
    };
    getFoto() {
        return this.foto;
    };
    setFoto(fots) {
        this.foto = fots;
    };
}