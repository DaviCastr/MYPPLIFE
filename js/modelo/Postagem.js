class Postagem {
    getComentario() {
        return this.comentario;
    }
    setComentario(cometr) {
        this.comentario = cometr;
    }
    getDescricao() {
        return this.descricao;
    }
    setDescricao(descr) {
        this.descricao = descr;
    }
    getId() {
        return this.id;
    }
    setId(ids) {
        this.id = ids;
    }
    getFoto() {
        return this.foto;
    }
    setFoto(ftp) {
        this.foto = ftp;
    }

}